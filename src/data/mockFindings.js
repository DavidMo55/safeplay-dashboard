export function simulateHuntProgress(onUpdate, hashtags) {
  return new Promise((resolve) => {
    let i = 0;
    function tick() {
      if (i >= HUNT_STAGES.length) {
        const data = mockHuntResponse(hashtags);
        onUpdate({
          status: "complete",
          message: `Cacería completa: ${data.findings_detected} amenazas detectadas`,
          ...data
        });
        resolve(data);
        return;
      }
      const stage = HUNT_STAGES[i++];
      onUpdate({ ...stage, hashtags });
      setTimeout(tick, stage.delay);
    }
    tick();
  });
}
